// Flatten all sounds into a single searchable array
export const ALL_SOUNDS = Object.values(SOUND_CATEGORIES).flatMap((cat) => cat.sounds);

// Remove duplicates
export const UNIQUE_SOUNDS = [...new Set(ALL_SOUNDS)].sort();

// Get display name from sound name (ENTITY_PLAYER_LEVELUP -> Entity Player Levelup)
export function getSoundDisplayName(sound) {
  return sound
    .split('_')
    .map((word) => word.charAt(0) + word.slice(1).toLowerCase())
    .join(' ');
}
